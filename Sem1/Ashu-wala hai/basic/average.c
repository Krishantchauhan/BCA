#include<stdio.h>

void main()
{

    int a,b,c,d;

    printf("Enter the value of A   ");
    scanf("%d",&a);

    printf("Enter the value of B   ");
    scanf("%d",&b);

    printf("Enter the value of C   ");
    scanf("%d",&c);

    d=(a+b+c)/3;

    printf("the average is = %d   ",d);

}


